    private List<Integer> findPermutation(String s, int B) {
        int i = 0, n = B;
        int maxIndex = 0;
        ArrayList<Integer> result = new ArrayList<>();
        if (!s.contains("I")) {
            for (int k = B; k > 0; k--) {
                result.add(k);
            }
            return result;
        }
        if (!s.contains("D")) {
            for (int k = 1; k <= B; k++) {
                result.add(k);
            }
            return result;
        }
        for (char c : s.toCharArray()) {
            if (i == B) {
                break;
            }
            if (c == 'D') {
                if (i == 0) {
                    result.add(maxIndex = i, n + 1);
                    result.add(i + 1, n);
                    i += 2;

                } else {
                    int tmpValue = result.get(maxIndex);
                    result.set(i - 1, result.get(maxIndex) + 1);
                    maxIndex = i - 1;
                    result.add(i, tmpValue);
                    i++;

                }


            } else if (c == 'I') {

                if (i == 0) {
                    result.add(i, n);
                    result.add(maxIndex = (i + 1), n + 1);
                    i += 2;
                } else {
                    result.add(i, result.get(maxIndex) + 1);
                    maxIndex = i;
                    i++;
                }

            }
            //  i+=2;
        }

        return result;
    }

    private List<Integer> findPermutationNew(String s, int B) {

        int min = 1;
        int max = B;
        char[] inputs = s.toCharArray();
        List<Integer> response = new ArrayList<>();

        for (int index = 0; index < inputs.length; index++) {
            char currentInput = inputs[index];

            if (currentInput == 'D') {
                response.add(max);
                max--;

            } else {
                response.add(min);
                min++;
            }
        }
        if(inputs[inputs.length-1]=='I'){
            response.add(max);
        }
        else {
            response.add(min);
        }
        return response;

    }
